(function () {
  // Define the window start time before the requires so we get a more accurate
  // window:start marker.
  const startWindowTime = Date.now();

  const electron = require('electron');
  const remote = require('@electron/remote');
  const path = require('path');
  const Module = require('module');
  const getWindowLoadSettings = require('../src/get-window-load-settings');
  const { getReleaseChannel } = require('../src/get-app-details.js');
  const StartupTime = require('../src/startup-time');
  const entryPointDirPath = __dirname;
  let blobStore = null;

  const startupMarkers = remote.getCurrentWindow().startupMarkers;

  if (startupMarkers) {
    StartupTime.importData(startupMarkers);
  }
  StartupTime.addMarker('window:start', startWindowTime);

  window.onload = async function () {
    try {
      StartupTime.addMarker('window:onload:start');
      const startTime = Date.now();
      await require('second-mate').ready;

      process.on('unhandledRejection', function (error, promise) {
        console.error(
          'Unhandled promise rejection %o with error: %o',
          promise,
          error
        );
      });

      // Normalize to make sure drive letter case is consistent on Windows
      process.resourcesPath = path.normalize(process.resourcesPath);

      // If the app booted from a custom V8 startup snapshot, wire the renderer
      // up to it: hand the snapshotted modules the real globals and serve them
      // to ordinary `require()` calls. No-op when there's no snapshot.
      setupSnapshot();

      setupAtomHome();
      const devMode =
        getWindowLoadSettings().devMode ||
        !getWindowLoadSettings().resourcePath.startsWith(
          process.resourcesPath + path.sep
        );

      const FileSystemBlobStore = require('../src/file-system-blob-store');
      // The seed (generated by script/generate-v8-cache-seed.js) ships at the
      // app root next to `static/`. It is read-only and only used when the
      // user's writable blob-store is empty (fresh install / post-update). Absent
      // in dev checkouts, where the fallback is simply not passed.
      const fsPlus = require('fs-plus');
      const bundledBlobStoreSeed = path.join(
        entryPointDirPath,
        '..',
        'blob-store-seed'
      );
      blobStore = FileSystemBlobStore.load(
        path.join(process.env.ATOM_HOME, 'blob-store'),
        fsPlus.isDirectorySync(bundledBlobStoreSeed)
          ? bundledBlobStoreSeed
          : undefined
      );

      const NativeCompileCache = require('../src/native-compile-cache');
      NativeCompileCache.setCacheStore(blobStore);
      NativeCompileCache.setV8Version(process.versions.v8);
      NativeCompileCache.install();

      if (getWindowLoadSettings().profileStartup) {
        profileStartup(Date.now() - startTime);
      } else {
        StartupTime.addMarker('window:setup-window:start');
        setupWindow().then(() => {
          StartupTime.addMarker('window:setup-window:end');
        });
        setLoadTime(Date.now() - startTime);
      }
    } catch (error) {
      handleSetupError(error);
    }
    StartupTime.addMarker('window:onload:end');
  };

  function setLoadTime(loadTime) {
    if (global.atom) {
      global.atom.loadTime = loadTime;
    }
  }

  function handleSetupError(error) {
    const currentWindow = remote.getCurrentWindow();
    currentWindow.setSize(800, 600);
    currentWindow.center();
    currentWindow.show();
    currentWindow.openDevTools();
    console.error(error.stack || error);
  }

  function setupWindow() {
    const CompileCache = require('../src/compile-cache');
    CompileCache.setAtomHomeDirectory(process.env.ATOM_HOME);
    // Point the compile cache at the read-only, pre-transpiled cache shipped
    // inside the app (generated by script/generate-compile-cache.js and placed
    // at the app root next to `static/`). Reads fall through to it when the
    // writable `$ATOM_HOME/compile-cache` misses, so the first launch after an
    // install/update gets cache hits instead of transpiling everything cold.
    // The directory is absent in dev checkouts, in which case the fallback is a
    // harmless no-op.
    const fs = require('fs-plus');
    const bundledCompileCache = path.join(
      entryPointDirPath,
      '..',
      'compile-cache'
    );
    if (fs.isDirectorySync(bundledCompileCache)) {
      CompileCache.setFallbackCacheDirectory(bundledCompileCache);
    }
    CompileCache.install(process.resourcesPath, require);

    const ModuleCache = require('../src/module-cache');
    ModuleCache.register(getWindowLoadSettings());

    require('document-register-element');

    const Grim = require('grim');
    const documentRegisterElement = document.registerElement;

    document.registerElement = (type, options) => {
      Grim.deprecate(
        'Use `customElements.define` instead of `document.registerElement` see https://javascript.info/custom-elements'
      );

      return documentRegisterElement(type, options);
    };

    const { userSettings, appVersion } = getWindowLoadSettings();

    const CSON = require('season');
    CSON.setCacheDir(path.join(CompileCache.getCacheDirectory(), 'cson'));

    const initScriptPath = path.relative(
      entryPointDirPath,
      getWindowLoadSettings().windowInitializationScript
    );
    const initialize = require(initScriptPath);

    StartupTime.addMarker('window:initialize:start');

    return initialize({ blobStore: blobStore }).then(function () {
      StartupTime.addMarker('window:initialize:end');
      electron.ipcRenderer.send('window-command', 'window:loaded');
    });
  }

  function profileStartup(initialTime) {
    function profile() {
      console.profile('startup');
      const startTime = Date.now();
      setupWindow().then(function () {
        setLoadTime(Date.now() - startTime + initialTime);
        console.profileEnd('startup');
        console.log(
          'Switch to the Profiles tab to view the created startup profile'
        );
      });
    }

    const webContents = remote.getCurrentWindow().webContents;
    if (webContents.devToolsWebContents) {
      profile();
    } else {
      webContents.once('devtools-opened', () => {
        setTimeout(profile, 1000);
      });
      webContents.openDevTools();
    }
  }

  function setupAtomHome() {
    if (process.env.ATOM_HOME) {
      return;
    }

    // Ensure ATOM_HOME is always set before anything else is required
    // This is because of a difference in Linux not inherited between browser and render processes
    // https://github.com/atom/atom/issues/5412
    if (getWindowLoadSettings() && getWindowLoadSettings().atomHome) {
      process.env.ATOM_HOME = getWindowLoadSettings().atomHome;
    }
  }

  // When the app boots from a custom V8 startup snapshot (generated by
  // script/generate-startup-snapshot.js and baked into v8_context_snapshot.bin),
  // a `snapshotResult` global is present with the pre-evaluated modules. This
  // hands those modules the renderer's real globals and routes ordinary
  // `require()` calls for them to the in-heap copies, so they never re-parse or
  // re-execute. A no-op when no snapshot is present (dev, or builds without one).
  function setupSnapshot() {
    if (typeof snapshotResult === 'undefined') {
      StartupTime.addMarker('window:snapshot-absent');
      return;
    }

    StartupTime.addMarker('window:snapshot-wire:start');

    // Give the snapshotted modules the live renderer globals (they were captured
    // with stand-ins during snapshot generation).
    snapshotResult.setGlobals(
      global,
      process,
      window,
      document,
      console,
      require
    );

    const customRequire = snapshotResult.customRequire;
    const definitions = customRequire.definitions || {};

    // Build an absolute-path → snapshot-key map WITHOUT evaluating anything.
    // The snapshot keys modules by path relative to `static/` with forward
    // slashes (e.g. "../src/text-editor.js"). Evaluating eagerly here would
    // force every snapshotted module to run its top-level code at startup —
    // defeating the snapshot's lazy-heap benefit — so we only materialize a
    // module when it's actually required (below).
    const staticDir = entryPointDirPath;
    const keyByAbsPath = new Map();
    for (const key of Object.keys(definitions)) {
      keyByAbsPath.set(path.resolve(staticDir, key), key);
    }

    // Lazily serve snapshotted modules from the heap on first require. We hook
    // Module._load (not _resolveFilename) and only intercept when the resolved
    // filename is one of ours, so ordinary resolution — including fs-plus's
    // resolveOnLoadPath for stylesheets — is untouched. customRequire memoizes,
    // so repeat requires are cheap and identity is preserved.
    let served = 0;
    const originalLoad = Module._load;
    Module._load = function (request, parent, isMain) {
      let resolved;
      try {
        resolved = Module._resolveFilename(request, parent, isMain);
      } catch (e) {
        return originalLoad.apply(Module, arguments);
      }
      const key = keyByAbsPath.get(resolved);
      if (key !== undefined) {
        if (!Module._cache[resolved]) {
          const exports = customRequire(key);
          const mod = new Module(resolved, parent);
          mod.exports = exports;
          mod.filename = resolved;
          mod.loaded = true;
          mod.paths = Module._nodeModulePaths(path.dirname(resolved));
          Module._cache[resolved] = mod;
          served++;
        }
        return Module._cache[resolved].exports;
      }
      return originalLoad.apply(Module, arguments);
    };

    // Record how many snapshot modules exist so benchmark runs can confirm the
    // snapshot was active. `served` accrues lazily as modules are required.
    StartupTime.addMarker(`window:snapshot-wired:${keyByAbsPath.size}`);
    if (process.env.PULSAR_LOG_SNAPSHOT === '1') {
      // Report the lazily-served count at the end of the tick.
      setImmediate(() =>
        console.log(`[snapshot] ${served}/${keyByAbsPath.size} modules served from heap`)
      );
    }
  }
})();
